#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*c_src;
	unsigned char	*c_dst;
	size_t			i;

	c_src = (unsigned char *) src;
	c_dst = (unsigned char *) dst;
	if (!c_dst && !c_src)
		return (0);
	i = 0;
	if (c_dst > c_src)
		while (len--)
			c_dst[len] = c_src[len];
	else
	{
		while (i < len)
		{
			c_dst[i] = c_src[i];
			i++;
		}
	}
	return (dst);
}
